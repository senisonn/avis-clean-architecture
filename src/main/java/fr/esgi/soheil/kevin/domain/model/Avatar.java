package fr.esgi.soheil.kevin.domain.model;

public class Avatar {

    private Long   id;
    private String name;

    public Avatar() {}

    public Long   getId()           { return id; }
    public void   setId(Long id)    { this.id = id; }
    public String getName()         { return name; }
    public void   setName(String n) { this.name = n; }
}
